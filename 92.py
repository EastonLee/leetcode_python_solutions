class Solution:
    def carFleet(self, target, position, speed):
        """
        :type target: int
        :type position: List[int]
        :type speed: List[int]
        :rtype: int
        """
        N = len(position)
        if N == 0: return 0
        time = [(target - position[i])/speed[i] for i in range(N)]
        idx = sorted(list(enumerate(position)), key=lambda x:x[1], reverse=True)
        idx = [i[0] for i in idx]
        position = [position[i] for i in idx]
        time = [time[i] for i in idx]
        res, peak = [time[0]], time[0]
        for i in range(1, N):
            if time[i] > peak:
                res.append(time[i])
                peak = time[i]
        return len(res)




