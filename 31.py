class Solution(object):

    def toGoatLatin(self, S):
        """
        :type S: str
        :rtype: str
        """
        S = S.split()
        def helper(w):
            if w[0] in 'aeiouAEIOU':
                return w + 'ma'
            else:
                return w[1:] + w[0] + 'ma'
        S = list(map(helper, S))
        N = len(S)
        S = [S[i] + 'a'*(i+1) for i in range(N)]
        return ' '.join(S)

