class Solution(object):
    def canVisitAllRooms(self, rooms):
        """
        :type rooms: List[List[int]]
        :rtype: bool
        """
        # return all(map(lambda x:x[0] in x[1], 
        #                 [(i+1,j) for i,j in enumerate(rooms[:-1])]))
        seen = {0}
        new_keys = rooms[0]
        while new_keys:
            k = new_keys.pop()
            seen.add(k)
            if len(seen) == len(rooms):
                return True
            for k1 in rooms[k]:
                if k1 not in seen:
                    new_keys.append(k1)
        return len(seen) == len(rooms)


